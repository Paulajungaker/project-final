import mongoose from "mongoose";

const formRequestSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const FormRequest = mongoose.model("FormRequest", formRequestSchema);

export default FormRequest;
